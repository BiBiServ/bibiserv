package de.unibi.cebitec.bibiserv.statistics.ws.response;

import org.primefaces.model.chart.ChartModel;

/**
 * Abstract data type representing a row of a database result.
 * Contains a category and its count.
 * 
 * @author Armin Toepfer - atoepfer(at)cebitec.uni-bielefeld.de
 */
public class SingleCategoryResult extends ChartModel {

    private String category;
    private Long count;

    public SingleCategoryResult() {
    }

    public SingleCategoryResult(String category, Long count) {
        this.category = category;
        this.count = count;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public Long getCount() {
        return count;
    }

    public void setCount(Long count) {
        this.count = count;
    }

    @Override
    public String toString() {
        return "SingleCategory: " + category + ", " + count;
    }
}
package de.unibi.techfak.bibiserv.biodom.exception;

public class NSNotSupportedException extends Exception {

    /**
     * 
     */
    private static final long serialVersionUID = 7821293939320822544L;

    public NSNotSupportedException() {
        super();
        
    }

    public NSNotSupportedException(String arg0) {
        super(arg0);
       
    }

    public NSNotSupportedException(String arg0, Throwable arg1) {
        super(arg0, arg1);
       
    }

    public NSNotSupportedException(Throwable arg0) {
        super(arg0);
       
    }

}
